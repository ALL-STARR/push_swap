#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*l;

	i = 0;
	l = lst;
	if (lst)
	{
		while (l != NULL)
		{
			l = l->next;
			i++;
		}
	}
	return (i);
}
